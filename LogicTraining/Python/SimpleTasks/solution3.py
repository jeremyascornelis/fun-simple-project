#perform arithmetic operation on two number
number1 = int(input("Enter first number: "))
number2 = int(input("Enter second number: "))

print(f"Addition of two numbers = {number1 + number2}")
print(f"Substraction of two numbers = {number1 - number2}")
print(f"Multiplication of two numbers = {number1 * number2}")
print(f"Division of two numbers (float) = {number1 / number2}")
print(f"Division of two numbers (int) = {number1 // number2}")
print(f"Modulus of two numbers = {number1 % number2}")